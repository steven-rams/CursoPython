# Ejercicio 1: Queremos hacer un programa informático que nos sume dos número y nos de el resultado en pantalla.


# Capturo los 2 numeros para realizar la suma
a = int(input("Leer un numero: "))
b = int(input("Leer otro numero: "))

# realizamos la operacion

suma = a + b 

# Mostramos en pantalla el resultado de la suma

print(f"Resultado de {a} + {b} = {suma}")